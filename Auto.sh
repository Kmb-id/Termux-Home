#!/usr/bin/env bash
# Termux Colorful ASCII Banner + Clear + Date + Nama di PS1
# Installer script — run sekali, tiap run bisa ganti nama (ENTER = pakai nama lama)

set -euo pipefail

# ==== Dependency check ====
command -v toilet >/dev/null 2>&1 || {
  echo "📦 Installing toilet..."
  pkg install toilet -y
}

command -v figlet >/dev/null 2>&1 || {
  echo "📦 Installing figlet..."
  pkg install figlet -y
}

BANNER_FILE="${HOME}/.config/termux-banner.sh"
mkdir -p "$(dirname "$BANNER_FILE")"

# ==== Input nama tiap kali run ====
NAME_FILE="${HOME}/.config/termux-name.txt"

# Warna untuk prompt

RED='\033[1;91m'
GREEN='\033[1;92m'
YELLOW='\033[1;93m'
BLUE='\033[1;94m'
PURPLE='\033[1;95m'
CYAN='\033[1;96m'
WHITE='\033[1;97m'
NC='\033[0m'

clear
echo -e "${CYAN}╔════════════════════════════════════╗${NC}"
echo -e "${CYAN}║  ⚙️ TERMUX THEMA STYLING V 0.3  🛠️   ║${NC}"
echo -e "${CYAN}╚════════════════════════════════════╝${NC}\n"
toilet -f term " 10 Random Automatic Themes " -F border --metal
# Input nama

if [[ -f "$NAME_FILE" ]]; then
OLD_NAME=$(<"$NAME_FILE")
echo -e "${WHITE}*  Prompt untuk PS1  *"
echo -e "${YELLOW}✅ NickName Tersimpan: ${CYAN}$OLD_NAME\n"
echo -e "${NC}⚠️ Apakah ada ingin mengganti NickName ???\n  Jika tidak lanjut ${GREEN}[Enter]${NC} saja . \n${PURPLE}👉 NickName New: ${NC} \c"
read -r USER_NAME
if [[ -z "$USER_NAME" ]]; then
USER_NAME="$OLD_NAME"
fi
else
echo -e "${WHITE}Buat NickName untuk ditampilkan di PS1:${NC} \n"
echo -e "${YELLOW} NickName :${CYAN} \c"
read -r USER_NAME
fi

echo "$USER_NAME" > "$NAME_FILE"

cat > "$BANNER_FILE" <<'EOF'
# ~/.config/termux-banner.sh
# Prints a random colorful ASCII banner on interactive shells.

case $- in
  *i*) ;; 
  *) return 0 2>/dev/null || exit 0 ;;
esac

clear

# ==== Gradient Printer ====
gradient_print () {
  local -a PALETTES=(
    "196 202 208 214 220 190 154 118 82 46 47 48 49 51 39 27 21 57 93 129 165 201"
    "201 198 200 129 93 57 21 27 39 45 51 50 49 48 47 46 82 118 154 190 220 214 208"
    "33 39 45 51 50 49 48 47 46 82 118 154 148 142 136 130 124 160 196"
    "51 87 123 159 195 231 195 159 123 87 51"
  )
  local pick=$((RANDOM % ${#PALETTES[@]}))
  local COLORS=( ${PALETTES[$pick]} )

  local i=0 line
  while IFS= read -r line; do
    local c=${COLORS[$(( i % ${#COLORS[@]} ))]}
    printf "\033[38;5;%sm%s\033[0m\n" "$c" "$line"
    i=$((i+1))
  done
}

